public class Summoners {

    private String sumName;
    private int sumLevel;
    private String sumTier;
    private String sumDivision;

    public String getSumName() {
        return sumName;
    }

    public void setSumName(String sumName) {
        this.sumName = sumName;
    }

    public int getSumLevel() {
        return sumLevel;
    }

    public void setSumLevel(int sumLevel) {
        this.sumLevel = sumLevel;
    }

    public String getSumTier() {
        return sumTier;
    }

    public void setSumTier(String sumTier) {
        this.sumTier = sumTier;
    }

    public String getSumDivision() {
        return sumDivision;
    }

    public void setSumDivision(String sumDivision) {
        this.sumDivision = sumDivision;
    }
}
